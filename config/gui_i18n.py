# 全局语言设置
_CURRENT_LANGUAGE = None

def set_language(lang):
    """设置界面语言"""
    global _CURRENT_LANGUAGE
    if lang in ['zh', 'en']:
        _CURRENT_LANGUAGE = lang

def get_system_language():
    """检测系统语言"""
    import os
    import locale
    
    # 方法1: 检查明确的环境变量
    env_lang = os.environ.get('GUI_LANG', '')
    if env_lang in ['zh', 'en']:
        return env_lang
    
    # 方法2: 检查LANG环境变量（改进逻辑）
    env_lang = os.environ.get('LANG', '')
    if env_lang.startswith('en'):
        return 'en'
    elif env_lang.startswith('zh'):
        return 'zh'
    
    # 方法3: 检查系统locale
    try:
        system_locale = locale.getdefaultlocale()[0] or ''
        if system_locale.startswith('en'):
            return 'en'
        elif system_locale.startswith('zh'):
            return 'zh'
    except:
        pass
    
    # 方法4: Windows系统语言检测
    try:
        import ctypes
        windll = ctypes.windll.kernel32
        language_id = windll.GetUserDefaultUILanguage()
        if language_id == 0x0409:  # English (US)
            return 'en'
        elif language_id & 0xFF == 0x04:  # Chinese family
            return 'zh'
    except:
        pass
    
    # 默认中文
    return 'zh'

def t_gui(key, **kwargs):
    """GUI文本翻译函数"""
    global _CURRENT_LANGUAGE
    
    # 确定语言
    if _CURRENT_LANGUAGE is None:
        lang = get_system_language()
    else:
        lang = _CURRENT_LANGUAGE
    
    # 获取翻译
    text = GUI_TRANSLATIONS.get(lang, {}).get(key, key)
    
    # 格式化参数
    if kwargs:
        try:
            return text.format(**kwargs)
        except:
            return text
    
    return text

# 向后兼容的别名
_ = t_gui

def is_english():
    """检测是否应该使用英文 - 兼容旧版config.i18n模块"""
    # 如果已经设置了全局语言，使用全局语言设置
    global _CURRENT_LANGUAGE
    if _CURRENT_LANGUAGE:
        return _CURRENT_LANGUAGE == 'en'
    
    # 否则检测系统语言
    system_lang = get_system_language()
    return system_lang == 'en'
