def get_text(*args, **kwargs):
    """
    è·å–æŒ‡å®šé”®çš„æœ¬åœ°åŒ–æ–‡æœ¬
    æ”¯æŒä¸¤ç§è°ƒç”¨æ–¹å¼ï¼š
    1. get_text(key, default=None, section='common')
    2. get_text(section, key) - ç”¨äºå‘åå…¼å®¹
    """
    try:
        # å¤„ç†ä¸åŒçš„è°ƒç”¨æ–¹å¼
        if len(args) == 2 and not kwargs:
            # get_text(section, key) è°ƒç”¨æ–¹å¼
            section, key = args
            default = None
        elif len(args) >= 1:
            # get_text(key, default=None, section='common') è°ƒç”¨æ–¹å¼
            key = args[0]
            default = args[1] if len(args) > 1 else kwargs.get('default', None)
            section = args[2] if len(args) > 2 else kwargs.get('section', 'common')
        else:
            # çº¯å…³é”®å­—å‚æ•°è°ƒç”¨
            key = kwargs.get('key')
            default = kwargs.get('default', None)
            section = kwargs.get('section', 'common')
            
        if not key:
            return default or ''
            
        # è·å–å½“å‰è¯­è¨€
        current_lang = get_current_language()
        
        # ç¡®ä¿æœ‰ç¿»è¯‘æ•°æ®
        if not TRANSLATIONS:
            load_translations()
        
        # æ£€æŸ¥æ˜¯å¦æœ‰è¯¥è¯­è¨€çš„ç¿»è¯‘
        if current_lang not in TRANSLATIONS:
            current_lang = 'en_US'
        
        # æ£€æŸ¥æ˜¯å¦æœ‰è¯¥section
        if section not in TRANSLATIONS[current_lang]:
            if default is not None:
                return default
            return key
        
        # æ£€æŸ¥æ˜¯å¦æœ‰è¯¥key
        if key not in TRANSLATIONS[current_lang][section]:
            # å°è¯•åœ¨è‹±æ–‡ä¸­æŸ¥æ‰¾
            if current_lang != 'en_US' and 'en_US' in TRANSLATIONS and section in TRANSLATIONS['en_US'] and key in TRANSLATIONS['en_US'][section]:
                result = TRANSLATIONS['en_US'][section][key]
                return result
            
            if default is not None:
                return default
            return key
        
        # è·å–ç¿»è¯‘å€¼
        result = TRANSLATIONS[current_lang][section][key]
        return result
        
    except Exception as e:
        if len(args) > 1:
            return args[1] if args[1] else args[0]
        return kwargs.get('default', kwargs.get('key', ''))


