def is_english(text: str) -> bool:
    """检查文本是否为英文"""
    if not text:
        return False
    # 简单的英文检测：如果文本主要包含ASCII字符，则认为是英文
    ascii_count = sum(1 for char in text if ord(char) < 128)
    return ascii_count / len(text) > 0.8
